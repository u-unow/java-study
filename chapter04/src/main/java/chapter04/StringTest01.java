package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\\temp");
		System.out.println("\"Hello\"");
		
		// 제어문자
		// \t |tab
		// \r |carrige return
		// \n | newline
		// \b | beep
		System.out.println("hello\tworld\nzz");
		System.out.println("hello");
		// '싱글포테이션
		char c = '\'';
		System.out.println(c);
	}

}
